    public ArrayList<vehicles> vehiclelist() {
        ArrayList<vehicles> vehiclelist = new ArrayList<>();
        try {
            Class.forName("com.mysql.jdbc.Driver");
            connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/fleetdb", "root", "");
            String sql = "SELECT * FROM vehicle";
            ps = connection.prepareStatement(sql);
            rs = ps.executeQuery();
            vehicles vehicle;
            while (rs.next()) {
                vehicle = new vehicles(rs.getString("vehicle_id"),
                        rs.getString("model"),
                        rs.getString("type"),
                        rs.getString("color"));
                vehiclelist.add(vehicle);
            }
        } catch (Exception e) {
            e.printStackTrace();
            //JOptionPane.showMessageDialog(null,e);

        }
        return vehiclelist;
    }

    public void show_vehicle() {
        int i;
        ArrayList<vehicles> list = vehiclelist();
        DefaultTableModel model = (DefaultTableModel) jTable_Display_Vehicle.getModel();
        Object[] row = new Object[4];
        for (i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getVehicle_id();
            row[1] = list.get(i).getModel();
            row[2] = list.get(i).getType();
            row[3] = list.get(i).getColor();
            model.addRow(row);
        }
    }

