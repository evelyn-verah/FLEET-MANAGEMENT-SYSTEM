    private void delete_driverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_driverActionPerformed
        // TODO add your handling code here:
        //The selected Row is deleted
        int selectedRow = jTable_Display_Vehicle.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable_Display_Vehicle.getModel();
        if (selectedRow >= 0) {
            model.removeRow(selectedRow);
        } else {
            JOptionPane.showMessageDialog(null, "ERROR");

        }
        try {
            connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/fleetdb", "root", "");
            String sql = "DELETE  FROM vehicle where vehicle_id=?'" + selectedRow + "'";
            ps = connection.prepareStatement(sql);
            String value = jComboBox_Driver_id.getSelectedItem().toString();
            ps.setString(1, value);
            ps.executeUpdate();
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_delete_driverActionPerformed

